; nasm -f bin dos_payload.asm -o dos_payload.bin
jmp start

start:
    ; set DS = CS for flat model
    mov ax, cs
    mov ds, ax
    mov es, ax

    mov dx, prompt
    mov ah, 09h
    int 21h

    ; read input (same length as stored)
    mov si, input
    mov cx, 96

read_loop:
    mov ah, 01h
    int 21h
    mov [si], al
    inc si
    loop read_loop

    mov si, input
    mov di, cipher
    mov bx, [state]
    mov cx, 96

enc_loop:
    call xorshift
    mov al, bl
    xor al, [si]
    mov [di], al
    inc si
    inc di
    loop enc_loop

    ; compare
    mov si, cipher
    mov di, stored
    mov cx, 96

cmp_loop:
    mov al, [si]
    cmp al, [di]
    jne fail
    inc si
    inc di
    loop cmp_loop

success:
    mov dx, msg_ok
    mov ah, 09h
    int 21h
    jmp exit

fail:
    mov dx, msg_fail
    mov ah, 09h
    int 21h

exit:
    mov ax, 4C00h
    int 21h

; XORSHIFT16
xorshift:
    mov ax, bx
    mov dx, ax
    shl dx, 7
    xor ax, dx

    mov dx, ax
    shr dx, 9
    xor ax, dx

    mov dx, ax
    shl dx, 8
    xor ax, dx

    mov bx, ax
    ret

